package com.xunyunedu.repair.pojo.model;

public class ShenHe {
    private Integer id;
    private Integer isShenhe;
    private String weixiugong;
    private String liyou;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getIsShenhe() {
        return isShenhe;
    }

    public void setIsShenhe(Integer isShenhe) {
        this.isShenhe = isShenhe;
    }

    public String getWeixiugong() {
        return weixiugong;
    }

    public void setWeixiugong(String weixiugong) {
        this.weixiugong = weixiugong;
    }

    public String getLiyou() {
        return liyou;
    }

    public void setLiyou(String liyou) {
        this.liyou = liyou;
    }
}
